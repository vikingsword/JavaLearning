public class AutoInc {
    public static void main(String[] args) {
        int i = 1;
        System.out.println("i = " + i++);
        System.out.println("i = " + i);

        System.out.println("-----------------");

        int j = 1;
        System.out.println("j = " + ++j);
        System.out.println("j = " + j);

        System.out.println("-----------------");





    }
}
